def get_phonetic(word_str):
    """Look up IPA for a word, trying various strategies."""
    # Direct lookup
    if word_str in IPA_DICT:
        return f"/{IPA_DICT[word_str]}/"
    # Phrase lookup
    if word_str in PHRASE_IPA:
        ipa = PHRASE_IPA[word_str]
        if ipa == "-":
            return ""  # category headers, no phonetic needed
        return f"/{ipa}/"
    # Case-insensitive lookup
    lower = word_str.lower()
    if lower in IPA_DICT:
        return f"/{IPA_DICT[lower]}/"
    if lower in PHRASE_IPA:
        ipa = PHRASE_IPA[lower]
        if ipa == "-":
            return ""
        return f"/{ipa}/"
    # Strip parentheses/punctuation and try
    cleaned = re.sub(r'[()!?]', '', word_str).strip()
    if cleaned in IPA_DICT:
        return f"/{IPA_DICT[cleaned]}/"
    if cleaned.lower() in IPA_DICT:
        return f"/{IPA_DICT[cleaned.lower()]}/"
    # For slash variants like "foot/feet"
    if '/' in word_str:
        parts = word_str.split('/')
        ipas = []
        for p in parts:
            p = p.strip()
            if p.lower() in IPA_DICT:
                ipas.append(IPA_DICT[p.lower()])
            else:
                ipas.append(p)
        return f"/{'/'.join(ipas)}/"
    return ""  # empty = not found


def fix_words():
    with open('data/words.json', 'r', encoding='utf-8') as f:
        data = json.load(f)

    words = data['words']
    fixed_count = 0
    merged_count = 0
    missing_ipa = []

    for w in words:
        wid = w['id']

        # Step 1: Fix split multi-word entries
        if wid in MERGE_FIXES:
            correct_word, correct_meaning = MERGE_FIXES[wid]
            old_word = w['word']
            w['word'] = correct_word
            w['meaning'] = correct_meaning
            # Update syllables to match new word
            w['syllables'] = [correct_word]
            merged_count += 1

        # Step 2: Fix phonetics
        new_phonetic = get_phonetic(w['word'])
        if new_phonetic:
            w['phonetic'] = new_phonetic
            fixed_count += 1
        else:
            # Keep a cleaned version as fallback
            missing_ipa.append((wid, w['word']))
            # At minimum, format it properly with slashes
            w['phonetic'] = f"/{w['word'].lower()}/"

    # Also fix entries where meaning has (US ...) or (UK ...) annotations
    # that got mangled - clean up meaning field
    for w in words:
        m = w['meaning']
        # Remove leading "(US center) " etc from meaning
        m = re.sub(r'^\(US [^)]+\)\s*', '', m)
        m = re.sub(r'^\(UK [^)]+\)\s*', '', m)
        w['meaning'] = m

    # Update total
    data['total'] = len(words)

    # Write back
    with open('data/words.json', 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"Fixed {fixed_count} phonetics")
    print(f"Merged {merged_count} split multi-word entries")
    if missing_ipa:
        print(f"\nMissing IPA for {len(missing_ipa)} words:")
        for wid, word in missing_ipa:
            print(f"  id={wid}: {word}")


if __name__ == '__main__':
    fix_words()
